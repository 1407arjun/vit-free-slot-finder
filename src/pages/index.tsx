import { Center, useCheckboxGroup, Grid, GridItem } from "@chakra-ui/react"
import type { NextPage } from "next"
import Head from "next/head"
import { useRouter } from "next/router"
import { useEffect, useState } from "react"
import CheckGroup from "../components/CheckGroup"
import SlotTable from "../components/SlotTable"
import formatContent from "../utils/formatContent"
import findFreeSlots from "vit-timetable-explorer"
import Slots from "vit-timetable-explorer/dist/src/types/slots"

const Home: NextPage = () => {
    const router = useRouter()
    const [content, setContent] = useState<{ [key: string]: string }>({})
    const [slots, setSlots] = useState<Slots>({})
    const { value, getCheckboxProps } = useCheckboxGroup()

    useEffect(() => {
        const data = localStorage.getItem("data")
        if (!data) router.replace("/upload")
        else setContent(formatContent(data))
    }, [])

    useEffect(() => {
        const data = value.map((v) => content[v])
        const freeSlots = findFreeSlots(data)
        setSlots(freeSlots)
    }, [value])

    return (
        <Center minH="100vh">
            <Head>
                <title>Create Next App</title>
                <meta
                    name="description"
                    content="Generated by create next app"
                />
                <link rel="icon" href="/favicon.ico" />
            </Head>
            <Grid
                templateColumns={{
                    base: "repeat(1, 1fr)",
                    lg: "repeat(4, 1fr)"
                }}
                w="100%"
                p={8}
                gap={8}>
                <GridItem colSpan={1}>
                    <CheckGroup
                        names={Object.keys(content)}
                        props={getCheckboxProps}
                    />
                </GridItem>
                <GridItem colSpan={3}>
                    <SlotTable slots={slots} />
                </GridItem>
            </Grid>
        </Center>
    )
}

export default Home
